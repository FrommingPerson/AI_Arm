def parse_gcode_line(line):
    coords = {}
    parts = line.strip().split()
    for part in parts:
        if part.startswith('X'):
            coords['x'] = float(part[1:])
        elif part.startswith('Y'):
            coords['y'] = float(part[1:])
        elif part.startswith('Z'):
            coords['z'] = float(part[1:])
    return coords

def reverse_coordinates(coords, x_center, y_center):
    # Reverse the coordinates around the center point of the limits
    coords['x'] = 2 * x_center - coords['x']
    coords['y'] = 2 * y_center - coords['y']
    return coords

def convert_gcode_to_json(gcode, x_limits=(-130, 120), y_limits=(240, 380)):
    strokes = []
    current_stroke = []
    
    # Calculate the center points for x and y
    x_center = (x_limits[0] + x_limits[1]) / 2
    y_center = (y_limits[0] + y_limits[1]) / 2
    
    for line in gcode:
        line = line.strip()
        
        if line.startswith('G1'):  # G1 command is for linear movement
            coords = parse_gcode_line(line)
            if 'x' in coords and 'y' in coords:
                # Reverse the coordinates
                reversed_coords = reverse_coordinates(coords, x_center, y_center)
                current_stroke.append({
                    "x": reversed_coords['x'],
                    "y": reversed_coords['y'],
                    "a": 0,
                    "p": 0.0
                })
        
        if line.startswith('G0 Z5'):  # New stroke, indicating a pen-up move
            if current_stroke:
                strokes.append(current_stroke)
                current_stroke = []
    
    # Add the last stroke if any
    if current_stroke:
        strokes.append(current_stroke)
    
    drawing = {"drawing": {"strokes": strokes}}
    return drawing

def gcode_to_json(gcode_lines, x_limits=(-130, 120), y_limits=(240, 380)):
    json_structure = convert_gcode_to_json(gcode_lines, x_limits, y_limits)
    return json.dumps(json_structure, indent=4)

