import json

def generate_drawing_json(phrase, letter_coordinates, spacing=7, space_adjustment=-6, line_height=-9):
    x_start = 0
    y_start = 0  # Start at 0, decrease by line_height for each new line
    
    drawing_json = {
        "drawing": {
            "strokes": []
        }
    }
    
    for letter in phrase:
        # Handle newline by resetting x and adjusting y
        if letter == '/':
            x_start = 0
            y_start -= line_height
            continue  # Skip processing for the newline itself
        
        # If the current character is a space, adjust x_start by space_adjustment
        if letter == ' ':
            x_start += space_adjustment
            continue  # Skip processing for the space itself
        
        # Get the strokes for the current letter
        strokes = letter_coordinates.get(letter, []).copy()  # Use copy to avoid modifying the original coordinates
        
        # If the letter doesn't have any strokes, continue to the next one
        if not strokes:
            continue
        
        # Adjust each stroke within the current letter
        adjusted_strokes = []
        for stroke in strokes:
            adjusted_stroke = []
            for point in stroke:
                adjusted_point = {
                    "x": (((point["x"]-70) * 0.25)+ 70) + 30 + x_start,  # Adjust x by the current x_start value
                    "y": (((point["y"]-300) * 0.25)+ 300) - 40 + y_start,  # Adjust y by the current y_start value
                    "a": point.get("a", 5),  # Assuming default 0 if 'a' not present
                    "p": point.get("p", -30.0)  # Assuming default 0.0 if 'p' not present
                }
                adjusted_stroke.append(adjusted_point)
            adjusted_strokes.append(adjusted_stroke)
        
        # Add the adjusted strokes to the strokes list in the JSON structure
        drawing_json["drawing"]["strokes"].extend(adjusted_strokes)
        
        # Decrement x_start by the spacing for the next letter
        x_start -= spacing

    # Convert the dictionary to a JSON string
    drawing_json_str = json.dumps(drawing_json, indent=4)
    
    with open("./data/path_data.json", 'w') as f:
        f.write(drawing_json_str)

    print("Drawing JSON file has been created successfully.")


